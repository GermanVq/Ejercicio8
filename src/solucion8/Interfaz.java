    private void cmdcalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdcalcularActionPerformed
     double totalp,nmh,total,desc;
     String res,res2;
     
     if(txtnumhoras.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(this,"Digite n√∫mero de horas","Error", JOptionPane.ERROR_MESSAGE);
         txtnumhoras.requestFocusInWindow();
     }else{
     
     nmh=Double.parseDouble(txtnumhoras.getText());
     
     totalp=nmh*20000;
     
     desc=totalp*0.05;
     total=totalp-desc;
     
     res=String.valueOf(desc);
     txtdescuento.setText(res);
     res2=String.valueOf(total);
     txttotal.setText(res2);
     }
    }//GEN-LAST:event_cmdcalcularActionPerformed

